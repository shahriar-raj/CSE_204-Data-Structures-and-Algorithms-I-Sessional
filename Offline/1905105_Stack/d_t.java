public class d_t {
    private int time,co_n,finish;

    d_t(int time, int co_n){
        this.time = time;
        this.co_n = co_n;
    }

    public int getTime() {
        return time;
    }

    public int getCo_n() {
        return co_n;
    }

    public void setFinish(int finish) {
        this.finish = finish;
    }

    public int getFinish() {
        return finish;
    }
}
